void solve() {
    int n;
    cin >> n;
    const int MOD = 1e9+7;

    ll ans = 1;
    for (int i = 0; i < n; i++) {
        ans <<= 1;
        ans %= MOD;
    }

    cout << ans << endl;

    return;
}

int main() {
    ios_base::sync_with_stdio(false);  
    cin.tie(NULL);
    srand(chrono::steady_clock::now().time_since_epoch().count()); 
    
    int t = 1;
    /* cin >> t; */
    while (t--) {
        solve();
    }

    return 0;
}
